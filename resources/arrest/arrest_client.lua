function arrest(command, thePlayer)
	IS_PLAYER_BEING_ARRESTED(true)
	PLAY_ENTITY_ANIM("random@arrests@busted")
	IS_PED_CUFFED(true)
	PLAY_ENTITY_ANIM("get_up@cuffed")
	PLAYER.CLEAR_PLAYER_WANTED_LEVEL(thePlayer)
	SET_PLAYER_WANTED_LEVEL_now(0)
	addEventHandler("arrest", arrest, thePlayer)
end
